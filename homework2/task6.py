# Создаем два пустых списка для хранения чисел
first_list = []
second_list = []

# Заполняем первый список тремя числами с клавиатуры
for i in range(3):
    num = int(input(f"Введите {i + 1}-е число для первого списка: "))
    first_list.append(num)

# Заполняем второй список семью числами с клавиатуры
for i in range(7):
    num = int(input(f"Введите {i + 1}-е число для второго списка: "))
    second_list.append(num)

# Расширяем первый список элементами из второго списка
first_list.extend(second_list)

# Преобразуем первый список во множество, чтобы оставить только уникальные элементы
first_set = set(first_list)

# Преобразуем обратно в список, чтобы сохранить порядок элементов
new_first_list = list(first_set)

# Выводим результат
print("Первый список:", new_first_list)
