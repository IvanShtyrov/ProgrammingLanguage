# Генерируем список роста для первого класса (160 см до 176 см с шагом 2).
class1_heights = list(range(160, 177, 2))

# Генерируем список роста для второго класса (162 см до 180 см с шагом 3).
class2_heights = list(range(162, 181, 3))

# Объединяем два списка роста в один.
combined_heights = class1_heights + class2_heights

# Сортируем объединенный список по возрастанию.
combined_heights.sort()

# Выводим отсортированный список на экран.
print(f"Отсортированный список учеников: {combined_heights}")
