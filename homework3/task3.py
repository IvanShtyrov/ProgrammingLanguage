def draw_frame(s, k):
    # Вычисляем длину строки s
    length = len(s)

    # Создаем верхнюю горизонтальную рамку
    top_border = k * (length + 4)  # Дополнительно +4 для учета 2 символов k с каждой стороны и 2 звездочек на краях

    # Выводим верхнюю горизонтальную рамку
    print(top_border)

    # Выводим боковые границы и строку s с символами k
    for line in s.split('\n'):
        print(f"{k} {line} {k}")

    # Создаем и выводим нижнюю горизонтальную рамку
    print(top_border)

# Пример использования:
text = "Текст в рамке"
frame_char = "*"
draw_frame(text, frame_char)
